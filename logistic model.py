import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

df = pd.read_csv("/content/titanic_preprocessed_new")
df.head()

x = df.drop(columns = ["PassengerId", "Survived", "Name", "Ticket"])
y = df["Survived"]
from sklearn.model_selection import train_test_split
x_train, x_test, y_train, y_test = train_test_split(x, y, test_size=0.2, random_state=67, stratify = y)

print(x_train.shape, y_train.shape)
print(x_test.shape, y_test.shape)

from sklearn.linear_model import LogisticRegression
model = LogisticRegression()

model.fit(x_train, y_train)

y_pred = model.predict(x_test)
print(y_pred)

from sklearn.metrics import accuracy_score
print("accuracy: ", accuracy_score(y_test, y_pred))

from sklearn.metrics import precision_score
print("precision: ", precision_score(y_test, y_pred)) #gives false positives

from sklearn.metrics import recall_score
print("recall: ", recall_score(y_test, y_pred)) #gives false negatives

from sklearn.metrics import f1_score
print("f1 score: ", f1_score(y_test, y_pred)) #harmonic mean of recall and precision

from sklearn.metrics import classification_report
print(classification_report(y_test, y_pred))

from sklearn.metrics import confusion_matrix, ConfusionMatrixDisplay
cm = confusion_matrix(y_test, y_pred)
print(cm)

disp = ConfusionMatrixDisplay(confusion_matrix=cm, display_labels=["Died", "Survived"])
disp.plot(cmap="Blues")
plt.title("Confusion Matrix - Logistic Regression")
plt.show()

y_proba = model.predict_proba(x_test)[:,1]

from sklearn.metrics import roc_curve, roc_auc_score, RocCurveDisplay
fpr, tpr, thresholds = roc_curve(y_test, y_proba)
roc_auc = roc_auc_score(y_test, y_proba)
print("auc: ", roc_auc)
print("fpr: ", fpr)
print("tpr: ", tpr)
print("thresholds: ", thresholds)

RocCurveDisplay.from_predictions(y_test, y_proba, name="Logistic Regression")
plt.plot([0, 1], [0, 1], linestyle="--", color="gray", label="Random guess (AUC=0.5)")
plt.title("ROC Curve - Logistic Regression")
plt.legend()
plt.show()

